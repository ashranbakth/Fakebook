CREATE VIEW VIEW_PHOTO_INFORMATION AS
SELECT DISTINCT A.ALBUM_ID, cast(A.ALBUM_OWNER_ID AS VARCHAR2(100)) AS OWNER_ID, A.COVER_PHOTO_ID, A.ALBUM_NAME, cast(A.ALBUM_CREATED_TIME AS TIMESTAMP(6)) AS ALUBM_CREATED_TIME, cast(A.ALBUM_MODIFIED_TIME AS TIMESTAMP(6)) AS ALBUM_MODIFIED_TIME, cast(A.ALBUM_LINK AS VARCHAR2(2000)) AS ALBUM_LINK, A.ALBUM_VISIBILITY, 
	P.PHOTO_ID, cast(P.PHOTO_CAPTION AS VARCHAR2(2000)) AS PHOTO_CAPTION, cast(P.PHOTO_CREATED_TIME AS TIMESTAMP(6)) AS PHOTO_CREATED_TIME, cast(P.PHOTO_MODIFIED_TIME AS TIMESTAMP(6)) AS PHOTO_MODIFIED_TIME, cast(P.PHOTO_LINK AS VARCHAR2(2000)) AS PHOTO_LINK
FROM PHOTOS P
LEFT JOIN ALBUMS A ON P.ALBUM_ID = A.ALBUM_ID;
----------------------------------------------------------------------------------------------
CREATE VIEW VIEW_TAG_INFORMATION AS
SELECT DISTINCT TAG_PHOTO_ID AS PHOTO_ID, cast(TAG_SUBJECT_ID AS VARCHAR2(100)) AS TAG_SUBJECT_ID, cast(TAG_CREATED_TIME AS TIMESTAMP(6)) AS TAG_CREATED_TIME, TAG_X AS TAG_X_COORDINATE, TAG_Y AS TAG_Y_COORDINATE
FROM TAGS;
----------------------------------------------------------------------------------------------
CREATE VIEW VIEW_EVENT_INFORMATION AS
SELECT DISTINCT cast(E.EVENT_ID AS VARCHAR2(100)) AS EVENT_ID, cast(E.EVENT_CREATOR_ID AS VARCHAR2(100)) AS EVENT_CREATOR_ID, E.EVENT_NAME, cast(E.EVENT_TAGLINE AS VARCHAR2(1000)) AS EVENT_TAGLINE, cast(E.EVENT_DESCRIPTION AS VARCHAR2(4000)) AS EVENT_DESCRIPTION, E.EVENT_HOST, E.EVENT_TYPE, E.EVENT_SUBTYPE, cast(E.EVENT_LOCATION AS VARCHAR2(200)) AS EVENT_LOCATION, 
	C.CITY_NAME AS EVENT_CITY, C.STATE_NAME AS EVENT_STATE, C.COUNTRY_NAME AS EVENT_COUNTRY, cast(E.EVENT_START_TIME AS TIMESTAMP(6)) AS EVENT_START_TIME, cast(E.EVENT_END_TIME AS TIMESTAMP(6)) AS EVENT_END_TIME
FROM USER_EVENTS E
LEFT JOIN CITIES C ON E.EVENT_CITY_ID = C.CITY_ID;





