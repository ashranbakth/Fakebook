CREATE VIEW USER_HC AS
SELECT DISTINCT U.USER_ID, U.FIRST_NAME, U.LAST_NAME, U.YEAR_OF_BIRTH, U.MONTH_OF_BIRTH, U.DAY_OF_BIRTH, U.GENDER, 
	H.HOMETOWN_CITY_ID
FROM USERS U
LEFT JOIN USER_HOMETOWN_CITY H ON H.USER_ID = U.USER_ID;

CREATE VIEW USER_HC_CITY AS
SELECT DISTINCT A.USER_ID, A.FIRST_NAME, A.LAST_NAME, A.YEAR_OF_BIRTH, A.MONTH_OF_BIRTH, A.DAY_OF_BIRTH, A.GENDER, 
	C.CITY_NAME AS HOMETOWN_CITY, C.STATE_NAME AS HOMETOWN_STATE, C.COUNTRY_NAME AS HOMETOWN_COUNTRY
FROM USER_HC A
LEFT JOIN CITIES C ON A.HOMETOWN_CITY_ID = C.CITY_ID;

CREATE VIEW USER_CC AS
SELECT DISTINCT U.USER_ID, U.FIRST_NAME, U.LAST_NAME, U.YEAR_OF_BIRTH, U.MONTH_OF_BIRTH, U.DAY_OF_BIRTH, U.GENDER, 
	C.CURRENT_CITY_ID
FROM USERS U
LEFT JOIN USER_CURRENT_CITY C ON C.USER_ID = U.USER_ID;

CREATE VIEW USER_CC_CITY AS
SELECT DISTINCT A.USER_ID, A.FIRST_NAME, A.LAST_NAME, A.YEAR_OF_BIRTH, A.MONTH_OF_BIRTH, A.DAY_OF_BIRTH, A.GENDER, 
	C.CITY_NAME AS CURRENT_CITY, C.STATE_NAME AS CURRENT_STATE, C.COUNTRY_NAME AS CURRENT_COUNTRY
FROM USER_CC A
LEFT JOIN CITIES C ON A.CURRENT_CITY_ID = C.CITY_ID;

CREATE VIEW USER_BOTH_CITY AS
SELECT DISTINCT A.USER_ID, A.FIRST_NAME, A.LAST_NAME, A.YEAR_OF_BIRTH, A.MONTH_OF_BIRTH, A.DAY_OF_BIRTH, A.GENDER, A.HOMETOWN_CITY, A.HOMETOWN_STATE, A.HOMETOWN_COUNTRY,
B.CURRENT_CITY, B.CURRENT_STATE, B.CURRENT_COUNTRY 
FROM USER_HC_CITY A
FULL JOIN USER_CC_CITY B ON A.USER_ID = B.USER_ID;

CREATE VIEW EP AS
SELECT DISTINCT E.USER_ID, E.PROGRAM_YEAR, 
	P.INSTITUTION, P.CONCENTRATION, P.DEGREE
FROM EDUCATION E
LEFT JOIN  PROGRAMS P ON E.PROGRAM_ID = P.PROGRAM_ID;

CREATE VIEW VIEW_USER_INFORMATION AS
SELECT DISTINCT cast(A.USER_ID AS VARCHAR2(100)) AS USER_ID, A.FIRST_NAME, A.LAST_NAME, cast(A.YEAR_OF_BIRTH AS NUMBER) AS YEAR_OF_BIRTH, cast(A.MONTH_OF_BIRTH AS NUMBER) AS MONTH_OF_BIRTH, cast(A.DAY_OF_BIRTH AS NUMBER) AS DAY_OF_BIRTH, A.GENDER,  A.CURRENT_CITY, A.CURRENT_STATE, A.CURRENT_COUNTRY, A.HOMETOWN_CITY, A.HOMETOWN_STATE, A.HOMETOWN_COUNTRY,
	B.INSTITUTION AS INSTUTUTION_NAME, cast(B.PROGRAM_YEAR AS NUMBER(38)) AS PROGRAM_YEAR, cast(B.CONCENTRATION AS CHAR(100)) AS PROGRAM_CONCENTRATION, B.DEGREE AS PROGRAM_DEGREE
FROM USER_BOTH_CITY A
LEFT JOIN EP B ON A.USER_ID = B.USER_ID;

